//стр психолога кнопка читать статью

function myFunctionReadArticle1() {
  var dotsReadArticle1 = document.getElementById("dots-read-article1");
  var moreReadArticle1 = document.getElementById("read-more-article1");
  var btnReadArticle1 = document.getElementById("myBtnReadArticle1");

  if (dotsReadArticle1.style.display === "none") {
    dotsReadArticle1.style.display = "inline";
    btnReadArticle1.innerHTML = "Читать";
    moreReadArticle1.style.display = "none";
  } else {
    dotsReadArticle1.style.display = "none";
    btnReadArticle1.innerHTML = "Свернуть";
    moreReadArticle1.style.display = "inline";
  }
}

